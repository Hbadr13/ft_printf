#include "ft_printf.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = -1;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[++i])
		write(fd, &s[i], fd);
	return (ft_strlen(s));
}
